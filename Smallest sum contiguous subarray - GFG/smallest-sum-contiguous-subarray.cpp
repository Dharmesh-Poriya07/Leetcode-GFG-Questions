class Solution{
  public:
  int smallestSumSubarray(vector<int>& a){
      int curr_sum = 0;
      int ans = INT_MAX;
      for(int i=0;a.size()>i;i++){
          if(0<curr_sum)
            curr_sum = a[i];
          else
            curr_sum += a[i];
          ans = min(ans,curr_sum);
      }
      return ans;
  }
};


