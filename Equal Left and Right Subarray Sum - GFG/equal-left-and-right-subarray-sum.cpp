#include<bits/stdc++.h>
using namespace std;


class Array
{
public:
    template <class T>
    static void input(vector<T> &A,int n)
    {
        for (int i = 0; i < n; i++)
        {
            scanf("%d ",&A[i]);
        }
    }
 
    template <class T>
    static void print(vector<T> &A)
    {
        for (int i = 0; i < A.size(); i++)
        {
            cout << A[i] << " ";
        }
        cout << endl;
    }
};


// } Driver Code Ends
class Solution {
  public:
    int equalSum(int N, vector<int> &A) {
        if(1==N) return 1;
        if(2==N) return -1;
        int total_sum = 0;
        for(int a : A) total_sum += a;
        int prefix_sum = 0;
        prefix_sum += A[0];
        for(int i=1;i<N-1;i++){
            int right = total_sum-prefix_sum-A[i];
            if(right == prefix_sum)
                return i+1;
            prefix_sum += A[i];
        }
        return -1;
    }
};


